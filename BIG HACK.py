import turtle as trt 
pen=trt.Turtle()
currx=150
curry=150
def circle(r):
    pen.penup()
    pen.right(90)
    pen.forward(r)
    pen.left(90)
    pen.pendown()
    pen.circle(r)
    pen.penup()
    pen.left(90)
    pen.forward(r)
    pen.right(90)
def rect(x,y,x1,y1):
    pen.penup()
    pen.goto(x,y)
    pen.pendown()
    pen.goto(x+x1,y)
    pen.goto(x+x1,y+y1)
    pen.goto(x,y+y1)
    pen.goto(x,y)
pen.pencolor("red")
rect(-100,0,200,100)
rect(-150,0,50,50)
rect(100,0,50,50)
pen.pencolor("blue")
rect(-100,50,100,50)
pen.penup()
pen.goto(-125,-25)
pen.pencolor("black")
circle(25)
pen.penup()
pen.goto(125,-25)
circle(25)
pen.goto(-300,-65)
pen.pensize(25)
pen.pendown()
pen.goto(300,-65)
misc=trt.Screen()
misc.mainloop()